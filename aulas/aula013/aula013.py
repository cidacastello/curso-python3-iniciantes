print('Programa para calcular a área de um terreno retangular', '\n')
# area = base * altura

# entrada de dados
base = float(input('Informe o valor da largura do terreno: '))
altura = float(input('Informe o valor da profundidade do terreno: '))

# cálculo da área - processamento
area = base * altura

# saída de dados
# print('A área do terreno é de', area, 'm2')
print('A área do terreno é de ' + str(area) + ' m2')

