# Op    Operação         Matemática
# ==    igualdade           =
# >     maior que           >
# <     menor que           <
# !=     diferente          ≠
# >=     maior ou igual     ≥
# <=     menor ou igual     ≤

# Exemplo de uso de operadores relacionais

a = 1
b = 5
c = 2
d = 1

print(f'a é igual a b ? {a == b}')
print(f'b é maior que a? {b > a}')
print(f'a é menor que b? {a < b}')
print(f'a é igual a d? {a == d}')
print(f'b é maior ou igual a a? {b >= a}')
print(f'c é menor ou igual a b? {c <= b}')
print(f'd é diferente de a? {d != a}')
print(f'd é diferente de b? {d != b}')





