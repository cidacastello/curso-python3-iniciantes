#  Operador | Operação | Precedência |
#  not      | não      | 1ª          |
#  and      | e        | 2ª          |
#  or       | ou       | 3ª          |

print('\nNOT')
print(not True)
print(not False)

print('\nAND')
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print('\nOR')
print(True or True)
print(True or False)
print(False or True)
print(False or False)
